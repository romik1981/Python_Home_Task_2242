"""
1. Выяснить тип результата выражений:
15 * 3
15 / 3
15 // 2
15 ** 2

"""


print(15 * 3) # Выводим на экран результат вычисления
print('Тип выражения 15 * 3: ', type(15 * 3)) # Определяем тип выражений с помощью функции type

print(15 / 3)
print('Тип выражения 15 / 3: ', type(15 / 3))

print(15 // 2)
print('Тип выражения 15 // 2: ', type(15 // 2))

print(15 ** 2)
print('Тип выражения 15 ** 2: ', type(15 ** 2))